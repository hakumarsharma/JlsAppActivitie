package com.example.nutapp.pojo;

public class ShareDeviceData {

    private String deviceName;
    private String deviceShareDetail;
    private String deviceShareDate;

    public String getDeviceName() {
        return deviceName;
    }

    public void setDeviceName(String deviceName) {
        this.deviceName = deviceName;
    }

    public String getDeviceShareDetail() {
        return deviceShareDetail;
    }

    public void setDeviceShareDetail(String deviceShareDetail) {
        this.deviceShareDetail = deviceShareDetail;
    }

    public String getDeviceShareDate() {
        return deviceShareDate;
    }

    public void setDeviceShareDate(String deviceShareDate) {
        this.deviceShareDate = deviceShareDate;
    }
}
